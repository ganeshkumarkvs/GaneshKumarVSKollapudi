package day2;

public class CheckTheNumberPostiveOrNegative {

	public static void main(String[] args) {
		int n = -40;
		if(n>0) {
			System.out.println(n + " is positive number");
		}else if(n<0) {
			
			System.out.println(n + " is negative number");
		}else {
			
			System.out.println(n + " is zero");
		}
}
}

